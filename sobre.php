			<h4 class="page_title">Tipos de Licen&ccedil;a de Software Livre</h4>
			<div class="hr dotted clearfix">&nbsp;</div>
		  <p>Todo software livre deve ser licenciado atrav&eacute;s de uma licen&ccedil;a de software livre. Uma das mais conhecidas &eacute; a GNU GPL. Entretanto, existem diversas outras: GNU AGPL, GNU LGPL, GNU FDL , MPL (Licen&ccedil;a p&uacute;blica Mozilla), Licen&ccedil;a Apache, Licen&ccedil;a MIT e Licen&ccedil;a BSD.</p>
          <h6>Software Livre e Software em Domínio Público</h6>
            <p>Software livre &eacute; diferente de software em dom&iacute;nio p&uacute;blico. O primeiro, quando utilizado em combina&ccedil;&atilde;o com licen&ccedil;as t&iacute;picas (como as licen&ccedil;as GPL e BSD),   garante a autoria do desenvolvedor ou organiza&ccedil;&atilde;o. O segundo caso   acontece quando se passam os anos previstos nas leis de cada pa&iacute;s de   prote&ccedil;&atilde;o dos direitos do autor e este se torna bem comum. Ainda assim,   um software em dom&iacute;nio p&uacute;blico pode ser considerado como um software   livre, desde que atenda as liberdades definidas pela Free Software   Foundation, como citadas anteriormente.</p>
            <h6>Software Livre e Copyleft</h6>
          <p>Licen&ccedil;as como a GPL cont&ecirc;m um conceito adicional, conhecido como <em>copyleft</em>. Ao contr&aacute;rio de <em>copyright</em>, em <em>copyleft</em> o autor cede alguns direitos. Por isto o termo left. Na pr&aacute;tica isto   significa que o autor continua sendo o dono, mas sua obra pode ser   utilizada/modificada/distribu&iacute;da por outras pessoas, respeitando termos   espec&iacute;ficos de licen&ccedil;a. Um software livre sem <em>copyleft</em> pode ser   transformado em n&atilde;o-livre por um usu&aacute;rio, caso assim o deseje. J&aacute; um   software livre protegido por uma licen&ccedil;a que ofere&ccedil;a <em>copyleft</em>, se distribu&iacute;do, dever&aacute; ser sob a mesma licen&ccedil;a, ou seja, repassando os direitos.</p>
          <p>Associando os conceitos de <em>copyleft</em> e software livre,   programas e servi&ccedil;os derivados de um c&oacute;digo livre devem obrigatoriamente   permanecer com uma licen&ccedil;a livre (os detalhes de quais programas, quais   servi&ccedil;os e quais licen&ccedil;as s&atilde;o definidos pela licen&ccedil;a original do   programa). O usu&aacute;rio, por&eacute;m, permanece com a possibilidade de n&atilde;o   distribuir o programa e manter as modifica&ccedil;&otilde;es ou servi&ccedil;os utilizados   para si pr&oacute;prio.</p> 
			
